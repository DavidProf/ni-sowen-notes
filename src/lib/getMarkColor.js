export default mark => {
    switch (mark) {
        case 1:
            return '#F8CECC';
        case 2:
            return '#FFF2CC';
        case 3:
            return '#DAE8FC';
        case 4:
            return '#D5E8D4';
        case 5:
            return '#CCCCCC';
        default:
            return '#F5F5F5'
    }
}